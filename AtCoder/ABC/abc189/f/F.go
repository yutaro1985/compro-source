type L struct {
	A, B float64
}

func main() {
	N, M, K := nextInt(), nextInt(), nextInt()
	// ゴールから戻していく
	// DP[i] = (DP[1]+DP[2]+…DP[i+j])/M+1
	// 振り出しに戻るところの場合 DP[i] = DP[0]
	// DP[0]（求めたい答え）をxとすると
	// DP[0] = a * x + b = x →x = b/(1-a)（a != 1）
	// このように、循環が発生する漸化式になる場合も、答えをxを置いて変換してみると循環のない漸化式に変換できることがある
	DP := make([]L, N+1)
	A := make([]bool, N)
	for i := 0; i < K; i++ {
		A[nextInt()] = true
	}
	var cnt int
	for i := 0; i < N; i++ {
		if A[i] {
			cnt++
		} else {
			cnt = 0
		}
		if cnt == M {
			fmt.Println(-1)
			return
		}
	}
	var sum L
	for i := N - 1; i >= 0; i-- {
		if A[i] {
			DP[i] = L{1, 0}
		} else {
			DP[i].A += sum.A / float64(M)
			DP[i].B += sum.B/float64(M) + 1
		}
		sum.A += DP[i].A
		sum.B += DP[i].B
		if i+M <= N {
			sum.A -= DP[i+M].A
			sum.B -= DP[i+M].B
		}
	}
	// 誤差が出る
	// if DP[0].A+1e-6 > 1 {
	if DP[0].A == 1 {
		fmt.Println(-1)
	} else {
		fmt.Println(DP[0].B / (1 - DP[0].A))
	}
}

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	sc.Scan()
	i, e := strconv.Atoi(sc.Text())
	if e != nil {
		panic(e)
	}
	return i
}

