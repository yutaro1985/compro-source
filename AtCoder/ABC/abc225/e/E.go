type Pair struct {
	x, y int
}

func (p *Pair) comp(a Pair) int {
	var res int
	x1, y1 := uint(p.x), uint(p.y)
	x2, y2 := uint(a.x), uint(a.y)
	// y1/x1 < y2/x2
	if y1*x2 > y2*x1 {
		res = 1
	} else if y1*x2 < y2*x1 {
		res = -1
	}
	return res
}

type Deg struct {
	L, R Pair
}

func main() {
	N := nextInt()
	P := make([]Pair, N)
	for i := 0; i < N; i++ {
		P[i] = Pair{nextInt(), nextInt()}
	}
	t := make([]Deg, N)
	for i := 0; i < N; i++ {
		t[i] = Deg{Pair{P[i].x - 1, P[i].y}, Pair{P[i].x, P[i].y - 1}}
	}
	sort.Slice(t, func(i, j int) bool {
		t1 := t[i].L
		t2 := t[j].L
		if t1.comp(t2) == 0 {
			t1R := t[i].R
			t2R := t[j].R
			return t1R.comp(t2R) < 0
		}
		return t1.comp(t2) < 0
	})
	ans := 1
	cur := t[0].L
	for i := 1; i < N; i++ {
		next := t[i].R
		LocalPrint(cur, next, cur.comp(next))
		if cur.comp(next) <= 0 {
			ans++
			cur = t[i].L
		}
	}
	fmt.Println(ans)
}

func nextLine() string {
	sc.Scan()
	return sc.Text()
}

func nextInt() int {
	sc.Scan()
	i, e := strconv.Atoi(sc.Text())
	if e != nil {
		panic(e)
	}
	return i
}

func nextFloat64() float64 {
	sc.Scan()
	f, e := strconv.ParseFloat(sc.Text(), 64)
	if e != nil {
		panic(e)
	}
	return f
}

func nextComplex128() complex128 {
	return complex(nextFloat64(), nextFloat64())
}

